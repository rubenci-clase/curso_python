# Comparadores en python

"""
Ejemplos de operadores de comparación en diferentes situaciones.
Comparando diferentes tipos de datos y observando cómo se comportan los operadores de igualdad en esos casos
"""

# Igualdad ==
x = 5
y = 7
print(x == y)  # Devolverá False

# Desigualdad !=
a = "hola"
b = "adiós"
print(a != b)  # Devolverá True

# Mayor que >
puntuacion_1 = 80
puntuacion_2 = 60
print(puntuacion_1 > puntuacion_2)  # Devolverá True

# Menor que <
edad = 25
limite_edad = 30
print(edad < limite_edad)  # Devolverá True

# Mayor o igual que >=
cantidad_1 = 10
cantidad_2 = 10
print(cantidad_1 >= cantidad_2)  # Devolverá True

# Menor o igual que <=
temperatura_actual = 25
temperatura_limite = 30
print(temperatura_actual <= temperatura_limite)  # Devolverá True

# Comparando diferentes tipos de datos
print("2 == 2:", 2 == 2)
print("2 == \"2\": ", 2 == "2")


