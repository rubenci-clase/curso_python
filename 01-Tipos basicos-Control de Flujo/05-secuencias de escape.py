# Secuencias de escape de Strings en python

"""
Secuencias de escape más comunes en Python. 
Para representar caracteres especiales y controlar el formato de las cadenas 
"""

# Nueva línea \n
print("Hola\nMundo")

# Tabulación \t
print("Nombre\tEdad")

# Retorno de carro \r
print("12345 \rabc")

# Barra invertida \\
print("Directorio: C:\\usuarios\\nombre")

# Comillas simples y dobles \'
print('Esto es una comilla simple: \'Hola\'')
print("Esto es una comilla doble: \"Mundo\"")

# Retroceso \b
print("abc\bde")

# Avance de página \f
print("Texto en la primera página\fTexto en la segunda página")
